#include <string.h>

char	*ft_strncat(char *s1, char *s2, size_t size)
{
	char *index;

	index = s1;
	while (*s1)
		s1++;
	while (*s2 && size)
	{
		*(s1++) = *(s2++);
		size--;
	}
	*s1 = '\0';
	return (index);
}
