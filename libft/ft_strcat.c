#include "libft.h"

char	*ft_strcat(char *s1, char *s2)
{
	char *index;

	index = s1;
	while (*s1)
		s1++;
	while (*s2)
		*(s1++) = *(s2++);
	*s1 = '\0';
	return (index);
}
